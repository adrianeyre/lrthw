# Set variables with escape keys
tabby_cat = "\tI'm tabbed in."
persian_cat = "I'm split\non a line."
backslash_cat = "I'm \\ a \\ cat."

# variable on more than one line using """
fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

# Display variables
puts tabby_cat
puts persian_cat
puts backslash_cat
puts fat_cat

# HEX value for 64
puts "\x64"
